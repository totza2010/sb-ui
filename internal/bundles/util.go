package bundles

import (
	"sort"
	"strconv"
)

func itoa(n int) string { return strconv.Itoa(n) }

func sortedKeys(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

